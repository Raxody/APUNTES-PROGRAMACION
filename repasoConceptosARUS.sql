-- Docker  -- Contenedores

virtualizacion de contenedores
entornos virtualizados aislados ahí se testea, programa usa y no hay peligro que se 
dañe.

Las imagenes en docker son como si fuesen unas plantillas,  se pueden encontrar dentro
SO, librerias, archivos binarios, herramientas, otros tipos de archivos, etc. 

Un contenedor es una copia de una imagen de docker -> entonces
Copia de una imagen de docker + aplicacion desplegada en ella = instancia ejecutable.

Estos contenedores se peuden trasladar de un pc a otro de un servidor a otro 
despreocupandome de todo porque todo se trabaja sobre el contenedor que se traiga
solo se debe tener instalado docker

Las imagenes en docker no dependen de los contenedores

Los contenedores en docker si dependen de las imagenes en docker 


