-- ¿Que es la nube o cloud?
    La nube son sistemas ordenadores o computadores que se encuentran desde otro 
    lugar en los cuales son accesibles en cualquier momento ya que manejan
    servidores de fondo y siempre estan funcionando estos manejan todo desde alla
    a la par que su internet tiene muchas capas o seguridades por si se cae una 
    entra otra y así 
    -- Cloud publica
      Que todos la pueden tener es como accesible a cualquiera y la comparten todos
    -- Cloud privata
        Cuando toda la infraestructura es para mi osea es personal, nadie más puede hacer
     uso de esta
    -- Cloud hibrida 
    Pueden haber partes privadas y partes publicas dejando lo que no es tan critico
    para que cualquiera la pueda usar

-- Que son los modelos cloud
    --Modelo de distribucion 
        --As a service aaS
            servicio o producto a traves de internet pago o gratiuito y que se
             puede redimensionar segun las necesidades del cliente
    --IaaS - Infraestructura como servicio
    Cuando se contratan y alquilo equipos informaticos con plataforma de virtualizacion
    con todos los poderes y esto se encuentra con el rpvoeedor el es el que se el
    encarga de proporcionar todo y aca uno tiene que cuadrar la infraestructura
    -- PaaS - plataforma como servicio
    Dedicada a desarrolladores o empresas de desarrolladores cuando entregan 
    infraestructura con funcionamiento ya realizado y ya uno solo la debe usar
    o trabajar sobre lo que le dan a uno sobre ella
    -- Saas - Software como servicio
    Servicio mas usado, cualquier servicio en la web para uso inmediato
    no se debe insatalar nada, ni mover nada lo unico es usarlo 

